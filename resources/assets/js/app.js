var ArticleBox = React.createClass({
    loadArticlesFromServer: function() {
        $.ajax({
            url: this.props.url ,
            cache: false,
            success: function(data) {
                console.log(data);
                this.setState({data: data});
            }.bind(this),
            error: function(xhr, status, err) {
                console.error(this.props.url, status, err.toString());
            }.bind(this)
        });
    },
    handleArticleSubmit: function(article) {
        var articles = this.state.data;
        // Optimistically set an id on the new article. It will be replaced by an
        // id generated by the server. In a production application you would likely
        // not use Date.now() for this and would have a more robust system in place.
        article.id = Date.now();
        var newArticles = articles.concat([article]);
        this.setState({data: newArticles});
        $.ajax({
            url: this.props.url,
            type: 'POST',
            data: article,
            success: function(data) {
                this.setState({data: data});
            }.bind(this),
            error: function(xhr, status, err) {
                this.setState({data: articles});
                console.error(this.props.url, status, err.toString());
            }.bind(this)
        });
    },
    getInitialState: function() {
        return {data: []};
    },
    componentDidMount: function() {
        this.loadArticlesFromServer();
        setInterval(this.loadArticlesFromServer, this.props.pollInterval);
    },
    render: function() {
        return (
            <div className="articleBox">
                <h1>Articles</h1>
                <hr />
                <ArticleList data={this.state.data} />
                <ArticleForm onArticleSubmit={this.handleArticleSubmit} />
            </div>
        );
    }
});

var ArticleList = React.createClass({
    render: function() {
        var articleNodes = this.props.data.map(function(article) {
            return (
                <Article title={article.title} key={article.id}>
                    {article.story}
                </Article>
            );
        });
        return (
            <div className="articleList">
                {articleNodes}
            </div>
        );
    }
});

var ArticleForm = React.createClass({
    getInitialState: function() {
        return {title: '', story: ''};
    },
    handleTitleChange: function(e) {
        this.setState({title: e.target.value});
    },
    handleStoryChange: function(e) {
        this.setState({story: e.target.value});
    },
    handleSubmit: function(e) {
        e.preventDefault();
        var title = this.state.title.trim();
        var story = this.state.story.trim();
        if (!story || !title) {
            return;
        }
        this.props.onArticleSubmit({title: title, story: story});
        this.setState({title: '', story: ''});
    },
    render: function() {
        return (
            <form className="articleForm" onSubmit={this.handleSubmit}>
                <input
                    type="text"
                    placeholder="Your name"
                    value={this.state.title}
                    onChange={this.handleTitleChange}
                />
                <br />
                <textarea
                    placeholder="Say something..."
                    value={this.state.story}
                    onChange={this.handleStoryChange}
                />
                <br />
                <input type="submit" value="Post" />
            </form>
        );
    }
});

ReactDOM.render(
    <ArticleBox url="../api/article" pollInterval={5000} />,
    document.getElementById('content')
);